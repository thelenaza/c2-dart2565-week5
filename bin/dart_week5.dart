void main(){
  print("Example of Arithemetic oparators");
  var a = 10;
  var b = 5;
  print(a+b);
  print( "a + b  = ${a+b} " );
  print("a - b = ${a-b}");
  print("a * b = ${a*b}"); 
  print("a / b = ${a/b}");
  print("a % b = ${a%b}");
  print("a ~/ b = ${a~/b}");
  print("-(a - b) = ${-(a-b)}");
}
